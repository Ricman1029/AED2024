# mensaje de inicio y carga de datos...
print('Calcular el área y el perímetro de un rectángulo.')
b = float(input('Ingrese el ancho del rectángulo: '))
h = float(input('Ingrese el alto del rectángulo: '))

# procesos...
area = b * h
perimetro = 2 * (h + b)

# resultados
print('El área del rectángulo es: ', area)
print('El perímetro del rectángulo es: ', perimetro)
