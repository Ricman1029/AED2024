num1 = float(input('Ingrese un número: '))
num2 = float(input('Ingrese otro número: '))

sumaCuad = num1 ** 2 + num2 ** 2
promCub = (num1 ** 3 + num2 ** 3) / 2

print('La suma de los cuadrados de los números ingresados es: ', sumaCuad)
print('El promedio de los cubos de los números ingresados es: ', promCub)